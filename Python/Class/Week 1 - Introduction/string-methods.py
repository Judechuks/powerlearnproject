str = "Hello, visitor."
print(str)

# Slice
print(str[0]) # prints the first letter
print(str[0:5]) # slices from the 1st letter to the 5th (index based) the last number (5) is not included
print(str[7:]) # slices from the 8th letter to the last (index based)

#Concatenation
print(str + " You are Welcome!")

# Repetition
print(str * 3)
