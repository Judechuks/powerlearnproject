void main() {
// declaring variables
  String name = "John"; //must be in quotes
  String address = "Kenya"; //must be in quotes
  num age = 20; //whole numbers
  num height = 5.9; //decimal numbers
  bool isMarried = false; //boolean

// NOTE: "int" is for whole numbers, "double" for decimal point numbers and "num" accepts both "int" and "double"

// printing variables value
  print("Name is $name");
  // concatenation; but you can't concatenate a String and a num datatype
  print("Address is " + address);
  print("Age is $age");
  print("Height is $height");
  print("Married Status is $isMarried");
}
